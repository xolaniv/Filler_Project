#include "libft.h"

size_t		ft_sqrt(size_t n)
{
	size_t i;

	i = 2;
	if (n == 0 || n == 2 || n == 3)
		return (0);
	if (n == 1)
		return (1);
	if (n % 2 == 0)
		while (n > i)
		{
			if (n / i == i && n % i == 0)
				return (i);
			i += 2;
		}
	else
	{
		i++;
		while (n > i)
		{
			if (n / i == i && n % i == 0)
				return (i);
			i += 2;
		}
	}
	return (0);
}
